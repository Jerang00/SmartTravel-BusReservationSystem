-- Insert Seats (for each bus)
INSERT INTO Seats (BusID, SeatNumber, SeatType)
SELECT 
    b.BusID,
    s.number as SeatNumber,
    CASE 
        WHEN s.number <= 2 THEN 'Premium'
        WHEN s.number <= 10 THEN 'Business'
        ELSE 'Economy'
    END as SeatType
FROM Buses b
CROSS JOIN (
    SELECT a.N + b.N * 10 + 1 as number
    FROM (SELECT 0 as N UNION SELECT 1 UNION SELECT 2 UNION SELECT 3 UNION SELECT 4 UNION SELECT 5 UNION SELECT 6 UNION SELECT 7 UNION SELECT 8 UNION SELECT 9) a
    CROSS JOIN (SELECT 0 as N UNION SELECT 1 UNION SELECT 2 UNION SELECT 3 UNION SELECT 4) b
    ORDER BY number
) s
WHERE s.number <= (SELECT Capacity FROM BusTypes WHERE BusTypeID = b.BusTypeID);

-- Insert Schedules
INSERT INTO Schedules (BusID, RouteID, DepartureTime, ArrivalTime)
SELECT 
    b.BusID,
    r.RouteID,
    ADDTIME('06:00:00', SEC_TO_TIME(FLOOR(RAND() * 43200) SECOND)) as DepartureTime,
    ADDTIME('06:00:00', SEC_TO_TIME(FLOOR(RAND() * 43200 + 43200) SECOND)) as ArrivalTime
FROM Buses b
CROSS JOIN Routes r
WHERE b.BusID <= 10
LIMIT 50;

-- Insert Pricing
INSERT INTO Pricing (RouteID, BusTypeID, BasePrice)
SELECT 
    r.RouteID,
    bt.BusTypeID,
    ROUND(r.Distance * (CASE 
        WHEN bt.TypeName LIKE '%Premium%' THEN 3.5
        WHEN bt.TypeName LIKE '%Deluxe%' THEN 3.0
        WHEN bt.TypeName LIKE '%AC%' THEN 2.5
        ELSE 2.0
    END), 2) as BasePrice
FROM Routes r
CROSS JOIN BusTypes bt
WHERE bt.BusTypeID <= 10;

