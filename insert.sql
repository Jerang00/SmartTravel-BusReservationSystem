SELECT 
    b.BusNumber, 
    bt.TypeName, 
    s.DepartureTime, 
    s.ArrivalTime, 
    r.Distance, 
    p.BasePrice,
    COUNT(ss.SeatID) as AvailableSeats
FROM Schedules s
JOIN Buses b ON s.BusID = b.BusID
JOIN BusTypes bt ON b.BusTypeID = bt.BusTypeID
JOIN Routes r ON s.RouteID = r.RouteID
JOIN Cities c1 ON r.SourceCityID = c1.CityID
JOIN Cities c2 ON r.DestinationCityID = c2.CityID
JOIN Pricing p ON r.RouteID = p.RouteID AND bt.BusTypeID = p.BusTypeID
LEFT JOIN SeatStatus ss ON s.ScheduleID = ss.ScheduleID AND ss.Status = 'Available'
WHERE c1.CityName = 'Chennai' 
AND c2.CityName = 'Coimbatore'
AND DATE(s.DepartureTime) = '2024-01-22'
GROUP BY s.ScheduleID, b.BusNumber, bt.TypeName, s.DepartureTime, s.ArrivalTime, r.Distance, p.BasePrice;

